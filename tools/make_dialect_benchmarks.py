# -*- coding: utf-8 -*-
"""Generate per-dialect benchmark dictionaries from data/all_regions.csv.

Tag policy mirrors the original Connacht benchmark (_benchmark.lua):
a word's expected IPA for dialect D = all rows whose tags intersect D's
region set, PLUS untagged rows (treated as pan-dialectal).

Usage: python tools/make_dialect_benchmarks.py
Writes _benchmark_munster.lua and _benchmark_ulster.lua to the repo root.
(_benchmark.lua remains the Connacht benchmark; not regenerated here.)
"""
import csv
import os
from collections import defaultdict

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

REGION_SETS = {
    "munster": {"Munster", "Kerry", "Cork", "Waterford"},
    "ulster": {"Ulster"},
    # connacht set kept for reference / future regeneration:
    # {"Connacht", "Galway", "Mayo", "Aran", "Cois-Fharraige", "Connemara"}
}


def lua_escape(s):
    return s.replace("\\", "\\\\").replace('"', '\\"')


def main():
    by_word = defaultdict(list)  # word -> [(tagset, ipa)]
    src = os.path.join(ROOT, "data", "all_regions.csv")
    with open(src, encoding="utf-8") as f:
        for row in csv.DictReader(f):
            tags = {t.strip() for t in row["tags"].split(";") if t.strip()}
            by_word[row["word"]].append((tags, row["ipa"]))

    for dialect, regions in REGION_SETS.items():
        entries = {}
        n_tagged = 0
        for word, rows in by_word.items():
            variants = []
            has_tagged = False
            for tags, ipa in rows:
                if tags & regions:
                    variants.append(ipa)
                    has_tagged = True
                elif not tags:
                    variants.append(ipa)
            # Only words with at least one dialect-tagged transcription are
            # scored: untagged-only words carry mixed/Connacht-convention IPA
            # (lenis lˠ/nˠ, retracted n̠ʲ) that punishes correct dialect rules.
            # Untagged rows still count as accepted alternate variants for
            # words that ARE tagged.
            if variants and has_tagged:
                n_tagged += 1
                # de-dup, preserve order; join with comma (harness splits on ,)
                seen, uniq = set(), []
                for v in variants:
                    if v not in seen:
                        seen.add(v)
                        uniq.append(v)
                entries[word] = ", ".join(uniq)

        out_path = os.path.join(ROOT, "_benchmark_%s.lua" % dialect)
        with open(out_path, "w", encoding="utf-8", newline="\n") as f:
            f.write("-- Auto-generated by tools/make_dialect_benchmarks.py\n")
            f.write("-- Source: data/all_regions.csv  Dialect: %s\n" % dialect)
            f.write("-- Policy: rows tagged %s, plus untagged (pan-dialectal) rows.\n"
                    % "/".join(sorted(regions)))
            f.write("return {\n")
            for word in sorted(entries):
                f.write('  ["%s"] = {expected = "%s"},\n'
                        % (lua_escape(word), lua_escape(entries[word])))
            f.write("}\n")
        print("%s: %d words (%d with dialect-tagged IPA) -> %s"
              % (dialect, len(entries), n_tagged, os.path.basename(out_path)))


if __name__ == "__main__":
    main()
